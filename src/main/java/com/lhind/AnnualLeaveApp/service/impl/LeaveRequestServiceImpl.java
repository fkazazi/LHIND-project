    public List<LeaveRequest> getAllLeavesOfUser(Integer userId){
        return leaveRequestRepository.getAllLeavesOfUser(userId);
    }

    public List<LeaveRequest> getAllPendingLeaves (){
        return leaveRequestRepository.getAllPendingLeaves();
    }

    public LeaveRequest getLeaveById (Integer id){
        return leaveRequestRepository.getById(id);
    }

    public void deleteLeave (Integer id) {
        if (!leaveRequestRepository.getById(id).isFlag())
            throw new IllegalStateException("You cannot delete this request!");
        else
            leaveRequestRepository.deleteById(id);
    }

    public LeaveRequest saveLeave(LeaveRequest leaveRequest, User user){
        leaveRequest.setUser(user);
        leaveRequestRepository.save(leaveRequest);

        String link = "http://localhost:8080/supervisor/viewRequest?leaveRequestId=" +leaveRequest.getId();
        List<User> supervisors = userRepository.findAll();
        for (User users : supervisors){
            if (users.getRole().name().equals(ApplicationRoles.SUPERVISOR.name()))
                    emailSender.send(users.getUsername(), buildEmail(users.getFirstName(), link));
        }
        return leaveRequest;
    }


    public void confirmRejectLeave(LeaveRequest leaveRequest, boolean flag, String message){
        leaveRequest.setFlag(flag);
        leaveRequest.setPending(false);
        leaveRequestRepository.save(leaveRequest);
        if (!leaveRequest.isFlag()){
            emailSender.send(leaveRequest.getUser().getUsername(), buildRejectionEmail(leaveRequest, message));
        } else if (leaveRequest.isFlag()){
            emailSender.send(leaveRequest.getUser().getUsername(), buildConfirmationEmail(leaveRequest));
        }

    }

